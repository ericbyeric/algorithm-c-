#include <bits/stdc++.h>
using namespace std;
int a;
double b;
char c;
int d,e;
double f;
int main(){
    scanf("%d %lf %c", &a, &b, &c);
    printf("%d\n", a);
    printf("%lf\n", b);
    printf("%c\n", c);

    // double 타입으로 부터 int타입 2개 생성하기
    scanf("%d.%d", &d, &e);
    printf("%d %d\n", d, e);
    
    scanf("%lf", &f);
    printf("%lf\n", f);
    
}